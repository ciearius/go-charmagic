type charDecoder_euc struct {
}

func (charDecoder_euc) DecodeOneChar(input []byte) (c uint16, remain []byte, err error) {
	if len(input) == 0 {
		return 0, nil, errEOB
	}
	first := input[0]
	remain = input[1:]
	c = uint16(first)
	if first <= 0x8D {
		return uint16(first), remain, nil
	}
	if len(remain) == 0 {
		return 0, nil, errEOB
	}
	second := remain[0]
	remain = remain[1:]
	c = c<<8 | uint16(second)
	if first >= 0xA1 && first <= 0xFE {
		if second < 0xA1 {
			err = errBadChar
		}
		return
	}
	if first == 0x8E {
		if second < 0xA1 {
			err = errBadChar
		}
		return
	}
	if first == 0x8F {
		if len(remain) == 0 {
			return 0, nil, errEOB
		}
		third := remain[0]
		remain = remain[1:]
		c = c<<0 | uint16(third)
		if third < 0xa1 {
			err = errBadChar
		}
	}
	return
}

func Match_euc_jp() *MultiByteMatcher {
	return &MultiByteMatcher{
		"EUC-JP",
		"ja",
		charDecoder_euc{},
		commonChars_euc_jp,
	}
}

